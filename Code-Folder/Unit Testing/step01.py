'''
A simple example of using Python Asserts. A Python Assert checks to see if a statement is true
'''

print("Messing with Asserts")
# Create a boolean Variable - Either True or False
condition = True
# Test if the condition is True
assert(condition)

#change condition for false
condition = False
assert(condition)

number = 5
assert(number >= 5)

number = 3
assert(number >= 5)